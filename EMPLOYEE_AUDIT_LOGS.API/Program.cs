
using EMPLOYEE_AUDIT_LOGS.Application;
using EMPLOYEE_AUDIT_LOGS.Infrastructure;

namespace EMPLOYEE_AUDIT_LOGS.API
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddScoped<IEmployeeAuditLogService, EmployeeAuditLogService>();
            builder.Services.AddScoped<IEmployeeAuditLogRepository, EmployeeAuditLogRepository>();

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
